package model;

/**
 *
 * @author Andressa
 */
public class Grade {
    private double grade;

    public Grade() {
    }

    public Grade(double grade) {
        this.grade = grade;
    }

    public double getGrade() {
        return grade;
    }

    public void setGrade(double grade) {
        this.grade = grade;
    }

    @Override
    public String toString() {
        return "Grade{" + "grade=" + grade + '}';
    }        
}
